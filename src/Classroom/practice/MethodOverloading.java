package Classroom.practice;

public class MethodOverloading {
    public static void main(String[] args) {

    }
    final public static int add(int a, int b){
        return  a + b;
    }
    final public static int add(int a, int b, int c){
        return  a + b + c;
    }
    final public static float add(float a, float b){
        return  a + b;
    }

}
